package com.vasu.java;

public class TypeCasting {

	public static void main(String[] args)
	{
		/*// implicit type casting
		
		int a=20;
		
		System.out.println(a);
		
		//convert to double
		
		double d=a;
		
		System.out.println(d);
		
*/
		
		//Explicit Type Casting
		
		double d=12.45;
		
		System.out.println(d);
		//convert to integer(numeric)
		
		int a=(int)d;
		
		System.out.println(a);
		
	}

}
